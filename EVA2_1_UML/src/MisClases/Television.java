
package MisClases;

public class Television {
    private int canal;
    private int volumen;
    private boolean power;
    
    public int getCanal(){
        return canal;  
    }
    public void setCanal(int val){
        canal = val;  
    }
    
    public int getVolumen(){
        return volumen;  
    }
    public void setVolumen(int val){
        volumen = val;  
    }
    
    public boolean getPower(){
        return power;  
    }
    public void setPower(boolean val){
        power = val;  
    }
}

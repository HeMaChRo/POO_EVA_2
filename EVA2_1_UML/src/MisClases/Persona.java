

package MisClases;

public class Persona {
    private int id;
    private int edad;
    private String nombre;
    
    public int getEdad(){
        return edad;  
    }
    public void setEdad(int val){
        edad = val;  
    }
    
    public int getId(){
        return id;  
    }
    public void setId(int val){
        id = val;  
    }
    
    public String getNombre(){
        return nombre;  
    }
    public void setNombre(String val){
        nombre = val;  
    }
}
